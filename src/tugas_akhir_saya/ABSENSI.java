    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
        String selectedMatakuliah = matakuliah.getSelectedItem().toString();    
        Connection con = TUGAS_AKHIR_SAYA.getkoneksi();
        String insertQuery = "INSERT INTO absensi (NIM, nama, kelas, matkul) VALUES (?, ?, ?, ?)";
        try (PreparedStatement preparedStatement = con.prepareStatement(insertQuery)) {
            preparedStatement.setString(1, nim.getText());
            preparedStatement.setString(2, nama.getText());
            preparedStatement.setString(3, kelas.getText());
            preparedStatement.setString(4, selectedMatakuliah);
            preparedStatement.executeUpdate();
            JOptionPane.showMessageDialog(this, "Anda Berhasil Absen", "Success", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Absen gagal. Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan. Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

